        <h2 style="text-align: center; font-size: 15pt; color: pink; 
            font-family: 'Broadway'; text-shadow: 1pt black;">VAPORWAVE</h2>
        <p>It is micro-genre of music in which artists sample 80s and 90s music by
            slowing it down "narcotically." The genre takes an approach similar
            to that of lounge-y, jazz elevator music, but with a very twisted remix. 
            A lot of the albums covers were 
            defined by cool, blue, pink, and purple color schemes with sort 
            of a late 80s and 90s feel. Occasionally using some neon related tones.
            Think Miami Vice meets Windows 98.</p>
        <p>Interestingly enough, we modeled this in the background with sort of
        a quadruple hue. We also chose Broadway as a font and pink for many of 
        text styles. The Admin menu pops out on the right side. Sort
        of homage to "Defying the establishment", a motto of Vaporwave, which
        often takes a satirical approach to itself and also consumer capitalism,
        and pop-culture. Otherwise, texts appear black, and we include white, see
        through boxes to contain most of the info.</p>
